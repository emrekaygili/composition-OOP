public class Urun {
    public String isim;
    public String katagori;
    public int fiyat;
    public int urunno;
    public int stok;


    public Urun(String isim, String katagori, int fiyat, int urunno, int stok) {
        this.isim = isim;
        this.katagori = katagori;
        this.fiyat = fiyat;
        this.urunno = urunno;
        this.stok = stok;
    }
}
